		static bool irq0_(bool ena, ORDER odr) noexcept
		{
			switch(odr) {
			// P30 (DS)
			// PB5
			// PE5
			// PG0
			case ORDER::FIRST:
				PORT3::PMR.B0 = 0;
				MPC::P30PFS.PSEL = 0;
				MPC::P30PFS.ISEL = ena;
				break;
			case ORDER::SECOND:
				PORTB::PMR.B5 = 0;
				MPC::PB5PFS.PSEL = 0;
				MPC::PB5PFS.ISEL = ena;
				break;
			case ORDER::THIRD:
				PORTE::PMR.B5 = 0;
				MPC::PE5PFS.PSEL = 0;
				MPC::PE5PFS.ISEL = ena;
				break;
			case ORDER::FOURTH:
				PORTG::PMR.B0 = 0;
				MPC::PG0PFS.PSEL = 0;
				MPC::PG0PFS.ISEL = ena;
				break;
			default:
				return false;
			}
			return true;
		}

